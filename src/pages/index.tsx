import type { NextPage } from 'next'
import Head from 'next/head'
import { ProductCart, ProductSearchList } from 'src/features'
import { LayoutDefault } from 'src/components'
import { getApolloClient } from 'src/lib'
import { gql } from '@apollo/client'

const GET_PRODUCTS = gql`
  query GetProduts {
    products {
      id
      name
      description
      price
      cover {
        id
        url
        height
        width
      }
    }
  }
`

export async function getServerSideProps() {
  const apolloClient = getApolloClient()

  const {
    loading,
    data: { products },
  } = await apolloClient.query({
    query: GET_PRODUCTS,
  })

  return {
    props: {
      loading,
      products: products,
    },
  }
}

type TPageHomeProps = {
  loading?: boolean
  products: []
}

const Home: NextPage<TPageHomeProps> = ({ loading, products }) => {
  return (
    <>
      <Head>
        <title>Store | The best of New York</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <LayoutDefault>
        {!loading && <ProductSearchList products={products} />}
      </LayoutDefault>

      <ProductCart />
    </>
  )
}

export default Home
